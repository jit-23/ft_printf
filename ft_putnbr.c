int     ft_putnbr(int nb)
{
        if (nb == -2147483648)
        {
                write(1,"-2147483648",11);
        }
        if (nb < 0)
        {
                write(1,"-",1);
                nb = -nb;
        }
        if (nb > 9)
        {
                ft_putnbr(nb / 10);
                nb = nb % 10;
        }
        nb  += 48;
        write(1,&nb,1);
        return (0);
}
